n1 = int(input('Digite o primeiro valor: '))
n2 = int(input('Digite o segundo valor:'))

sum = n1 + n2
multi = n1 * n2
d = n1 / n2
di = n1 // n2
exp = n1 ** n2

print('A soma é {}, a multiplicação é {}, e a divisão é {}'.format(sum,multi,d))
print('Divisão inteira {} e potência {}'.format(di, exp))

